"""Public contracts for object-style template expressions."""

from __future__ import annotations

from dataclasses import dataclass

from pykara.declaration import Scope


@dataclass(frozen=True, slots=True)
class ExpressionObjectSpecification:
    """Describe one root object exposed to inline expressions."""

    name: str
    available_scopes: frozenset[Scope]
    description: str


@dataclass(frozen=True, slots=True)
class ExpressionPropertySpecification:
    """Describe one public property exposed by an expression object."""

    object_name: str
    name: str
    type: str
    available_scopes: frozenset[Scope]
    source_variable: str | None
    description: str


LINE_SCOPES = frozenset({Scope.LINE, Scope.WORD, Scope.SYL, Scope.CHAR})
WORD_SCOPES = frozenset({Scope.WORD, Scope.SYL, Scope.CHAR})
ALL_SCOPES = frozenset(
    {Scope.SETUP, Scope.LINE, Scope.WORD, Scope.SYL, Scope.CHAR}
)


